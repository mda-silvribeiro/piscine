int	ft_str_is_alpha(char *str)
{
	int ct;

	ct = 0;
	if (str[ct] == '\0')
	{
		return (1);
	}
	while (str[ct] != '\0')
	{
		if ((str[ct] < 'A' || str[ct] > 'Z') &&
			(str[ct] < 'a' || str[ct] > 'z'))
		{
			return (0);
		}
		ct++;
	}
	return (1);
}
