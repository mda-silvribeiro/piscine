#include <unistd.h>

void	aux(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int a;
	int b;

	a = -1;
	while (++a <= 98)
	{
		b = a;
		while (++b <= 99)
		{
			aux((char)(a / 10) + '0');
			aux((char)(a % 10) + '0');
			aux(' ');
			aux((char)(b / 10) + '0');
			aux((char)(b % 10) + '0');
			if ((a != 98))
			{
				aux(',');
				aux(' ');
			}
		}
	}
}
